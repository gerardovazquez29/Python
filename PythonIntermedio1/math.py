
import math

resultado = math.floor(2.8)
print(resultado) # 2 redondea hacia abajo

resultado1 = math.ceil(2.8)
print(resultado1) # 3 redondea hacia arriba

resultado2 = math.log(25, 5)
print(resultado2) # 2.0 logaritmo en base 5 de 25

resultado3 = math.log10(25)
print(resultado3) # 1.3979400086720377 logaritmo en base 10 de 25


resultado4 = math.sqrt(math.pi)
print(resultado4) # 1.7724538509055159 raiz cuadrada de pi

resultado5 = math.factorial(7)
print(resultado5) # 5040 factorial de 7

